//**자주 쓰이는 이벤트 */
// 이벤트 타입	     이벤트 발생 시점

// click	        마우스 버튼을 클릭했을 때
// dbclick	        마우스 버튼을 더블 클릭했을 때
// mousedown	    마우스 버튼을 눌렀을 때
// mouseup	        누르고 있던 마우스 버튼을 놓았을 때
// mousemove	    마우스를 움직였을 때
// mousenter	    마우스를 요소 안으로 이동했을 때
// mouseleave	    마우스를 요소 밖으로 이동했을 때


// 이벤트  타입	 이벤트 발생 시점

// keydown	        모든 키를 눌렀을 때 발생한다.
//                 * control, option, shift, tab, delete, 방향 키와 문자, 숫자, 특수 문자 키를 눌렀을 때 발생한다. 단, 문자, 숫자, 특수 문자 키를 눌렀을 때는 연속적으로 발생하지만 그 외의 키를 눌렸을 때는 한번만 발생한다.
// keypress	        문자 키를 눌렀을 때 연속적으로 발생한다.
//                 * control, option, shift, tab, delete, 방향 키 등을 눌렸을 때는 발생하지 않고 문자, 숫자, 특수 문자 키를 눌렸을 때만 발생한다.
// keyup	        누르고 있던 키를 놓았을 때 한번만 발생한다.
//                 * control, option, shift, tab, delete, 방향 키와 문자, 숫자, 특수 문자 키를 놓았을 때 발생한다.

